class Calculator():

    def suma(operand1, operand2):
        result = operand1 + operand2
        return result

    def resta(operand1, operand2):
        
        result = operand1 - operand2
        return result

    def multiplicacion(operand1, operand2):
        
      
        result = operand1 * operand2
        return result
    